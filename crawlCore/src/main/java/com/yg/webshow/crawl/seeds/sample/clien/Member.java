package com.yg.webshow.crawl.seeds.sample.clien;

public class Member {
	private String nick ;
	private String nickImageUrl ;
	private String userId ;
	
	public String getNick() {
		return nick;
	}
	public void setNick(String nick) {
		this.nick = nick;
	}
	public String getNickImageUrl() {
		return nickImageUrl;
	}
	public void setNickImageUrl(String nickImageUrl) {
		this.nickImageUrl = nickImageUrl;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
}
